/*
Лямбда-функции в C++ - это анонимные функции, которые могут быть созданы и использованы непосредственно в коде.
Этот код демонстрирует три различных примера использования лямбда-функций в C++. В первом примере создается простая лямбда-функция без параметров, которая просто выводит "Hello, world!" в консоль. Во втором примере создается лямбда-функция, которая принимает два параметра и возвращает их сумму. В третьем примере лямбда-функция захватывает переменные x и y из окружающей области видимости и возвращает их произведение.
*/


//--------------------------------------------------------------------------------
// Подключаемые файлы
//--------------------------------------------------------------------------------
#include <iostream>

//--------------------------------------------------------------------------------
// Главная программа
//--------------------------------------------------------------------------------
int main(int argc, char *argv[])
{
    int A;

    // Пример 1: Простая лямбда-функция без параметров
    auto sayHello = []() {
        std::cout << "Hello, world!" << std::endl;
    };

    // Вызов лямбда-функции
    sayHello();

    // Пример 2: Лямбда-функция с параметрами и возвращаемым значением
    auto add = [](int a, int b) -> int {
        return a + b;
    };

    // Вызов лямбда-функции с передачей параметров
    int result = add(3, 5);
    std::cout << "Result of addition: " << result << std::endl;

    // Пример 3: Захват переменных из окружающей области видимости
    int x = 10;
    int y = 20;
    auto multiply = [x, y]() -> int {
        return x * y;
    };

    // Вызов лямбда-функции
    int multiplicationResult = multiply();
    std::cout << "Result of multiplication: " << multiplicationResult << std::endl;

    return A;
}

